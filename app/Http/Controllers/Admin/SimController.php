    {
        $sims = Sim::latest()
            ->when($request->employee_id, function ($query) use ($request) {
                $query->where("added_by", $request->employee_id);
            })->with(["store","addedBy"])->get();

        return view("admin.sims.index", compact("sims"));
    }

